export class Product {
  constructor(id, name, type, price, color, status, condition, yearOfMaking) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.price = price;
    this.color = color;
    this.status = status;
    this.condition = condition;
    this.yearOfMaking = yearOfMaking;
  }
}
